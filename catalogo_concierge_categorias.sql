CREATE OR REPLACE VIEW vw_categorias_concierge AS
SELECT
  n1.codigo AS nivel_1_codigo,
  n1.nombre AS nivel_1_nombre,
  n2.codigo AS nivel_2_codigo,
  n2.nombre AS nivel_2_nombre,
  n3.codigo AS nivel_3_codigo,
  n3.nombre AS nivel_3_nombre,
  CONCAT(n1.codigo, ' - ', n1.nombre, ' > ', n2.codigo, ' - ', n2.nombre, ' > ', n3.codigo, ' - ', n3.nombre) AS ruta_categoria
FROM categorias_nivel_1 n1
INNER JOIN categorias_nivel_2 n2 ON n2.nivel_1_codigo = n1.codigo
INNER JOIN categorias_nivel_3 n3 ON n3.nivel_2_codigo = n2.codigo
WHERE n1.activo = 1 AND n2.activo = 1 AND n3.activo = 1;
